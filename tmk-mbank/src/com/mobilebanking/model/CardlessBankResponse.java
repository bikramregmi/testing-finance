package com.mobilebanking.model;

public class CardlessBankResponse {

	private Long cardlessBankId;
	
	private String cardlessBankName;

	public Long getCardlessBankId() {
		return cardlessBankId;
	}

	public void setCardlessBankId(Long cardlessBankId) {
		this.cardlessBankId = cardlessBankId;
	}

	public String getCardlessBankName() {
		return cardlessBankName;
	}

	public void setCardlessBankName(String cardlessBankName) {
		this.cardlessBankName = cardlessBankName;
	}
	
}
